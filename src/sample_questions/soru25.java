package sample_questions;

public class soru25 {

    public static void main(String[] args) {

        /*
            Soru 25-)
                Alfabeyi konsola yazdırın.
                örnek:
                a b c .. .. .. .. y z
                ipucu: char kullanmak iyi bir fikir olabilir
         */

        System.out.println("Alfabe :");

        for (char i = 'a'; i <= 'z' ; i++) {

            System.out.print(i+ " ");
        }
        System.out.println("\n");

    }
}
